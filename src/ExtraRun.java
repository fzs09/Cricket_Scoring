public class ExtraRun {
    
    int extra = 0;
    int nos = 0;
    int wides = 0;
    int bys = 0;
    int lBys = 0;
    int isExtra = 0;
    
    void setExtra(int r){
    
        extra = extra + r;
    }
    
    int getExtra(){
    
        return extra;
    }
    
    void setIsExtra(int a){
    
        isExtra = a;
    }
    
    int getIsExtra(){
    
        return isExtra;
    }
    
    void setNos(int r){
    
        nos = nos + r;
    }
    
    int getNos(){
    
        return nos;
    }
    
    void setWides(int r){
    
        wides = wides + r;
    }
    
    int getWides(){
    
        return wides;
    }
    
    void setBys(int r){
    
        bys = bys + r;
    }
    
    int getBys(){
    
        return bys;
    }
    
    void setLbys(int r){
    
        lBys = lBys + r;
    }
    
    int getLbys(){
    
        return lBys;
    }
    
        
    
}
